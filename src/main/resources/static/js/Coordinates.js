function setPolygonEvents1(polygon, mouseoverColor, mouseoutColor) {
    kakao.maps.event.addListener(polygon, 'mouseover', function () {
        // 해당 폴리곤의 색상을 mouseoverColor로 변경
        polygon.setOptions({ fillColor: mouseoverColor });
    });
    kakao.maps.event.addListener(polygon, 'mouseout', function () {
        // 해당 폴리곤의 색상을 mouseoutColor로 복원
        polygon.setOptions({ fillColor: mouseoutColor });
    });
}

function createAndDisplayPolygon1(map, paths, mouseoverColor, mouseoutColor) {
    var polygon = new kakao.maps.Polygon({
        map: map,
        path: paths,
        strokeWeight: 2,
        strokeColor: '#004c80',
        strokeOpacity: 0.8,
        fillColor: '#fff',
        fillOpacity: 0.7
    });

    // 각 폴리곤에 이벤트 리스너 설정
    setPolygonEvents1(polygon, mouseoverColor, mouseoutColor);
    return polygon; // 생성된 폴리곤 반환
}



